def to_taiwan_chinese(value: Any) -> Any:
    """把字串內的大陸用語 / 簡體字替換成台灣繁中等價字詞。

    輸入若不是字串（None、數字、bool）就原樣回傳。對 dict / list 請呼叫
    `apply_to_strings` 遞迴處理。
    """
    if not isinstance(value, str) or not value:
        return value
    out = value
    for cn, tw in _CN_TO_TW:
        if cn and cn != tw and cn in out:
            out = out.replace(cn, tw)
    return out


def apply_to_strings(value: Any) -> Any:
    """遞迴對 dict / list 裡的字串套用 `to_taiwan_chinese`。

    用來處理 LLM 回的結構化 JSON（藥物百科、檢驗報告、藥袋辨識結果），
    一次把所有字串欄位都洗過一遍。
    """
    if isinstance(value, str):
        return to_taiwan_chinese(value)
    if isinstance(value, dict):
        return {k: apply_to_strings(v) for k, v in value.items()}
    if isinstance(value, list):
        return [apply_to_strings(v) for v in value]
    return value


